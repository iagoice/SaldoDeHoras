import Foundation

extension Int16 {
    func isPositive() -> Bool {
        return self > 0
    }
}

extension Int {
    func isPositive() -> Bool {
        return self > 0
    }
}

extension CGFloat {
    func isPositive() -> Bool {
        return self > 0
    }
}
